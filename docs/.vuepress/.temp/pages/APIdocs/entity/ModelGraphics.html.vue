<h2 id="属性" tabindex="-1"><a class="header-anchor" href="#属性" aria-hidden="true">#</a> 属性</h2>
<p><strong><a href="">articulations : PropertyBag</a></strong></p>
<p>设置/获取模型关节。（见CZML Model articulation 示例）它的键由关节名称，单个空格和阶段名称组成，值是数字属性。</p>
<p><strong><a href="">clampAnimations : Property</a></strong></p>
<p>设置/获取指定glTF动画是否应在没有关键帧的持续时间内保持最后一个姿势。</p>
<p>Default Value: <code v-pre>true</code></p>
<p><strong><a href="">clippingPlanes : Property</a></strong></p>
<p>设置/获取<a href="https://www.vvpstk.com/public/Cesium/Documentation/ClippingPlaneCollection.html" target="_blank" rel="noopener noreferrer"><code v-pre>ClippingPlaneCollection</code><ExternalLinkIcon/></a> 属性。用于有选择地禁用渲染模型。</p>
<p><strong><a href="">color : Property</a></strong></p>
<p>设置/获取指定Color<a href="https://www.vvpstk.com/public/Cesium/Documentation/Color.html" target="_blank" rel="noopener noreferrer"><code v-pre>Color</code><ExternalLinkIcon/></a>与模型的渲染颜色混合的属性。</p>
<p>Default Value: <code v-pre>Color.WHITE</code></p>
<p><strong><a href="">colorBlendAmount : Property</a></strong></p>
<p>设置/获取混合模式的强度值（0-1）。</p>
<p>Default Value: <code v-pre>0.5</code></p>
<p><strong><a href="">colorBlendMode : Property</a></strong></p>
<p>设置/获取颜色混合模式。</p>
<p>Default Value: <code v-pre>ColorBlendMode.HIGHLIGHT</code></p>
<p><code v-pre>readonly</code> <strong><a href="">definitionChanged : Event</a></strong></p>
<p>获取每当更改或修改属性或子属性时引发的事件。</p>
<p>distanceDisplayCondition : Property</p>
<p>设置/获取显示条件<a href="https://www.vvpstk.com/public/Cesium/Documentation/DistanceDisplayCondition.html" target="_blank" rel="noopener noreferrer"><code v-pre>DistanceDisplayCondition</code><ExternalLinkIcon/></a>,即在距相机某个距离内显示。</p>
<p>heightReference : Property</p>
<p>设置/获取相对于地形的位置<a href="https://www.vvpstk.com/public/Cesium/Documentation/HeightReference.html" target="_blank" rel="noopener noreferrer"><code v-pre>HeightReference</code><ExternalLinkIcon/></a>。</p>
<p>Default Value: <code v-pre>HeightReference.NONE</code></p>
<p><strong><a href="">imageBasedLightingFactor : Property</a></strong></p>
<p>指定基于漫反射和镜面反射图像的照明的属性<a href="https://www.vvpstk.com/public/Cesium/Documentation/Cartesian2.html" target="_blank" rel="noopener noreferrer"><code v-pre>Cartesian2</code><ExternalLinkIcon/></a> 。</p>
<p><strong><a href="">incrementallyLoadTextures : Property</a></strong></p>
<p>在加载模型后纹理是否可以继续流入，默认是true，据说是在动态修改贴图的时候使用的。</p>
<p><strong><a href="">lightColor : Property</a></strong></p>
<p>指定光源颜色<a href="https://www.vvpstk.com/public/Cesium/Documentation/Cartesian3.html" target="_blank" rel="noopener noreferrer"><code v-pre>Cartesian3</code><ExternalLinkIcon/></a>。</p>
<p><strong><a href="">maximumScale : Property</a></strong></p>
<p>设置/获取模型最大比例尺大小. 在这个比例尺内模型模型大小缩放受<a href="https://www.vvpstk.com/public/Cesium/Documentation/ModelGraphics.html#minimumPixelSize" target="_blank" rel="noopener noreferrer"><code v-pre>ModelGraphics#minimumPixelSize</code><ExternalLinkIcon/></a>限制， 超过这个比例尺，不受<a href="https://www.vvpstk.com/public/Cesium/Documentation/ModelGraphics.html#minimumPixelSize" target="_blank" rel="noopener noreferrer"><code v-pre>ModelGraphics#minimumPixelSize</code><ExternalLinkIcon/></a>限制. .</p>
<p><strong><a href="">minimumPixelSize : Property</a></strong></p>
<p>设置/获取 模型的最小像素大小，而不考虑缩放。</p>
<p>Default Value: <code v-pre>0.0</code></p>
<p><strong><a href="">nodeTransformations : Property</a></strong></p>
<p>置要应用于此模型的节点转换集。这表示为 <a href="https://www.vvpstk.com/public/Cesium/Documentation/PropertyBag.html" target="_blank" rel="noopener noreferrer"><code v-pre>PropertyBag</code><ExternalLinkIcon/></a> ，其中的键是节点的名称， 值是 <a href="https://www.vvpstk.com/public/Cesium/Documentation/TranslationRotationScale.html" target="_blank" rel="noopener noreferrer"><code v-pre>TranslationRotationScale</code><ExternalLinkIcon/></a> 属性，用于描述要应用于该节点的转换。 转换是在glTF中指定的节点现有转换之后应用的，并且不会替换节点现有转换。</p>
<p><strong><a href="">runAnimations : Property</a></strong></p>
<p>设置/获取指定是否应该启动模型中指定的gltf动画。</p>
<p>Default Value: <code v-pre>true</code></p>
<p><strong><a href="">scale : Property</a></strong></p>
<p>设置/获取放大比例。</p>
<p>Default Value: <code v-pre>1.0</code></p>
<p><strong><a href="">shadows : Property</a></strong></p>
<p>设置/获取 从每个光源投射或接收阴影的模式。</p>
<p>Default Value: <code v-pre>ShadowMode.ENABLED</code></p>
<p><strong><a href="">show : Property</a></strong></p>
<p>设置/获取显隐性。</p>
<p>Default Value: <code v-pre>true</code></p>
<p><strong><a href="">silhouetteColor : Property</a></strong></p>
<p>设置/获取指定的模型边框颜色 <a href="https://www.vvpstk.com/public/Cesium/Documentation/Color.html" target="_blank" rel="noopener noreferrer"><code v-pre>Color</code><ExternalLinkIcon/></a>。</p>
<p>Default Value: <code v-pre>Color.RED</code></p>
<p><strong><a href="">silhouetteSize : Property</a></strong></p>
<p>设置/获取边框大小（像素）。</p>
<p>Default Value: <code v-pre>0.0</code></p>
<p><strong><a href="">uri : Property</a></strong></p>
<p>设置/获取模型路径。</p>
